class Resp(object):

    def __init__(self):
        '''
        Resp Parameters
        '''
        self.code=None
        self.desc=None
        
    def getCode(self): 
	    return self.code
	
    def setCode(self, code):
	    self.code = code
	

    def getDesc(self):
	    return self.desc
	
    def setDesc(self, desc):
	    self.desc = desc